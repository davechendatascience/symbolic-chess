"""Self-play iter-1 distillation.

Architecture A from `docs/math_engine_cpp_v0.md`: take a corpus generated by
math-engine-cpp self-play, label each position with Stockfish (the ground-
truth reference), and run PySR with a joint target so the new expression is
shaped by both the engine's outcomes AND a strong external eval.

Joint target:
    y_joint = 0.5 * z(decay_outcome) + 0.5 * z(stockfish_cp)
where z(.) is z-score normalization over TRAIN.

Pipeline:
  1. Load self-play parquet (from cpp_engine_self_play.py)
  2. Subsample positions (drop random-opening plies, deduplicate, cap)
  3. Label subsample with Stockfish at depth D
  4. Encode FENs → feature bank (same as iter-0)
  5. Build joint target, TRAIN/TEST split
  6. Fit baselines (constant, material LR, full LR)
  7. Run PySR with +,-,*,/,min,max + tanh,abs,sign
  8. Report Pareto front evaluated against BOTH targets separately
  9. Save winning eq as strategies/<id>_iter1.json + a markdown report
"""
from __future__ import annotations
